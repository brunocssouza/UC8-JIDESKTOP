    private void mostrarDetalhesButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mostrarDetalhesButtonActionPerformed
        // TODO add your handling code here:

        int indexSelecionado = table.getSelectedRow();
        if (indexSelecionado != -1) {
            int row = table.getSelectedRow();
            int col = table.getSelectedColumn();

            String dado = tableModel.getValueAt(row, col).toString();
            System.out.println("Dado selecionado: " + dado);
        }
    }//GEN-LAST:event_mostrarDetalhesButtonActionPerformed

    private void removerButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removerButton1ActionPerformed
        // TODO add your handling code here:
        int indexSelecionado = table.getSelectedRow();
        if (indexSelecionado != -1) {
            tableModel.removeRow(indexSelecionado);
        }
    }//GEN-LAST:event_removerButton1ActionPerformed

    private void alterarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_alterarButtonActionPerformed
        // TODO add your handling code here:
        int indexSelecionado = table.getSelectedRow();
        if (indexSelecionado != -1) {
            int row = table.getSelectedRow();
            int col = table.getSelectedColumn();
            
            String text = alterarField.getText();
            tableModel.setValueAt(text, row, col);
        }
    }//GEN-LAST:event_alterarButtonActionPerformed

